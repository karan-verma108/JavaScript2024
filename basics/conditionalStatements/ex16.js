// switch statement

const day = 'Tuesday';

switch (day) {
  case 'Monday':
    console.log('Yes it is monday');
    break;
  case 'Tuesday':
    console.log('Yes it is tuesday');
    break;
  case 'Wednesday':
    console.log('Yes it is wednesday');
    break;

  default:
    console.log('Please enter a valid day');
    break;
}
